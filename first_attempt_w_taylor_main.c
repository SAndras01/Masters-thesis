double sin_taylor(double x, double scaler_param)
{
    double scaler_pows[4];
    double x_pows[4];

    pow_3_5_7_9(scaler_param, &scaler_pows[0], &scaler_pows[1], &scaler_pows[2], &scaler_pows[3]);
    pow_3_5_7_9(x, &x_pows[0], &x_pows[1], &x_pows[2], &x_pows[3]);

    return x / scaler_param
           - x_pows[0] / (6 * scaler_pows[0])
           + x_pows[1] / (120 * scaler_pows[1])
           - x_pows[2] / (5040 * scaler_pows[2])
           + x_pows[3] / (362880 * scaler_pows[3]);
}

